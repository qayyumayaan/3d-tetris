function updateMatrix() {
  const randomRowIndex = Math.floor(Math.random() * matrix.length);
  const randomColumnIndex = Math.floor(Math.random() * matrix[randomRowIndex].length);
  matrix[randomRowIndex][randomColumnIndex] = matrix[randomRowIndex][randomColumnIndex] === 0 ? 1 : 0;
}

let counter = 0;
const interval = setInterval(() => {
  updateMatrix();
  counter++;
  if (counter === 1000) {
    clearInterval(interval);
  }
}, 1000);

export { matrix, updateMatrix };
